public class Portos {
    int index;
    int x;
    int y;

    public Portos(int index, int x, int y){
        this.index = index;
        this.x = x;
        this.y = y;
    }

    public int getIndex() {
        return index;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
}
